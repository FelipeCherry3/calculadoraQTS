public class Calculadora {

    /**
     *
     * @param a primeiro valor para operação matemática
     * @param b segundo valor para operação matemática
     * @return resultado da operação matemática Soma
     */
    public int soma (int a, int b){
        return a + b;
    }

    /**
     *
     * @param a primeiro valor para operação matemática
     * @param b segundo valor para operação matemática
     * @return resultado da operação matemática Subtração
     */
    public int subtracao (int a, int b){
        return a - b;
    }

    /**
     *
     * @param a primeiro valor para operação matemática
     * @param b segundo valor para operação matemática
     * @return resultado da operação matemática Multiplicação
     */
    public int multi (int a, int b){
        return a * b;
    }

    /**
     *
     * @param a primeiro valor para operação matemática
     * @param b segundo valor para operação matemática
     * @return resultado da operação matemática Divisao
     */
    public float divisao (float a, float b){
        return a / b;
    }

    /**
     *
     * @param a primeiro valor para operação matemática
     * @param b segundo valor para operação matemática
     * @return resultado da operação matemática Divisao
     */
    public int divisao (int a, int b){
        return a / b;
    }
}
