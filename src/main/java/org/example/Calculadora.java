package org.example;

public class Calculadora {

    public int soma (int a, int b){
        return a + b;
    }
    public int subtracao (int a, int b){
        return a - b;
    }
    public int multi (int a, int b){
        return a * b;
    }
    public float divisao (float a, float b){
        return a / b;
    }
    public int divisao (int a, int b){
        return a / b;
    }
}
